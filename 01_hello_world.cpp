// C++의 가장 기본 프로그램입니다.
// "프로그램의 시작점"인 main 함수와
// 화면에 글자를 출력하는 방법을 배웁니다.

#include <iostream>  // 표준 입출력(콘솔 출력/입력) 기능을 사용하기 위한 헤더

using namespace std;

// int main()은 프로그램이 시작되는 함수입니다.
// 운영체제는 main 함수가 끝날 때 반환되는 값을 보고
// 프로그램이 정상 종료되었는지 판단합니다.
int main() {
    // cout은 "콘솔에 출력"할 때 사용합니다.
    // << 연산자는 데이터를 출력 스트림으로 보내는 역할입니다.
    cout << "Hello, C++!" << endl;

    // return 0; 은 "정상 종료"를 의미합니다.
    return 0;
}
